s = "Hey there ! What should this string be ?"
#just a little practice on String characters
print ("The number of i into the string is %d" % s.count("i"))
print ("Its length is %s" % len(s))
print ("The print from the 1st letter to the 6th (actually the 7th if we are strict) one gives you: %s" % s[:7])
print ("If we rewrite the print with only uppercases : %s" % s.upper())